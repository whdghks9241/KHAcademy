package com.kh.finalSample;

public class music {

	private String type;
	
	private String emotion;
	
	public music(String type, String emotion) {
		this.emotion = emotion;
		this.type = type;
	}

	
	public String getType() {
		return type;
	}

	public String getEmotion() {
		return emotion;
	}

	
	

	

}
